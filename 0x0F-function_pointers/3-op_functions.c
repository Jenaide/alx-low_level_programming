#include "3-calc.h"

/**
 * op_add - function that returns the sum of a and b.
 * @a: input int
 * @b: input int
 * Return: results of sum a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that returns the subtract of a and b.
 * @a: input int
 * @b: input int
 * Return: results subtracted a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that returns the multiply of a and b.
 * @a: int
 * @b: int
 * Return: result of a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that returns the divide of a and b.
 * @a: int
 * @b: int
 * Return: results of a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that returns the module of a and b.
 * @a: int
 * @b: int
 * Return: results of a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
